def estimated_intervention(region):
    "Estimate intervention factor based on currently implemented interventions."
    file = "./InterventionsImplemented.csv"
    interventions_df = pd.read_csv(file)
    rows = interventions_df['CountryName'] == region

    q1 = 1.
    for measure in ['mandatorylockdown','publiceventsbanned','schoolsclosed']:
        if np.any(interventions_df[measure][rows]):
            q1 = q1*intervention_effects[measure]
    for measure in ['socialdistancing','quarantinepositives']:
        # Assume social distancing and positive case quarantine everywhere?
        q1 = q1*intervention_effects[measure]
    return 1-q1

def moving_average(x, w):
    return np.convolve(x, np.ones(w), 'valid') / w

def load_time_series(region,smooth=False,use_test_data=False):
    """
    Fetch data and parse to get time series of confirmed cases and deaths.
    For now, only works with JHU data.
    """
    if region in US_state_population.keys():
        if use_test_data:
            us_states_df = nytimes_data(source_file='./test_data/us-states.csv')
        else:
            us_states_df = nytimes_data()
        rows = us_states_df['state'] == region
        some_dates = list(us_states_df['date'][rows])
        data_dates = pd.date_range(some_dates[0],some_dates[-1])
        cum_deaths = list(us_states_df['deaths'][rows])
        cum_cases = list(us_states_df['cases'][rows])
    else:
        if use_test_data:
            test_files = ('./test_data/time_series_covid19_confirmed_global.csv','./test_data/time_series_covid19_deaths_global.csv')
            cases_df, deaths_df, data_dates = jhu_data(source_files=test_files)
        else:
            cases_df, deaths_df, data_dates = jhu_data()
        rows = cases_df['Country/Region'].isin([region])
        cum_cases = [cases_df[day.strftime('%-m/%-d/%y')][rows].sum() for day in data_dates]
        if not np.any(rows==True):
            raise(Exception)
        rows = deaths_df['Country/Region'].isin([region])
        cum_deaths = [deaths_df[day.strftime('%-m/%-d/%y')][rows].sum() for day in data_dates]
    if smooth:
        cum_cases = np.array(cum_cases)
        cum_deaths = np.array(cum_deaths)
        daily_cases = np.insert(np.diff(cum_cases),0,cum_cases[0])
        daily_deaths = np.insert(np.diff(cum_deaths),0,cum_deaths[0])
        smooth_cases = np.zeros_like(daily_cases)
        smooth_deaths = np.zeros_like(daily_deaths)

        smooth_cases[0] = 0.5*(daily_cases[0]+daily_cases[1])
        smooth_cases[1:-1] = moving_average(daily_cases, 3)
        smooth_cases[-1] = 0.5*(daily_cases[-1]+daily_cases[-2])

        smooth_deaths[0] = 0.5*(daily_deaths[0]+daily_deaths[1])
        smooth_deaths[1:-1] = moving_average(daily_deaths, 3)
        smooth_deaths[-1] = 0.5*(daily_deaths[-1]+daily_deaths[-2])
        return data_dates, np.cumsum(smooth_cases), np.cumsum(smooth_deaths)
    else:
        return data_dates, np.array(cum_cases), np.array(cum_deaths)


